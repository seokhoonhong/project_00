package com.hong.vo;

public class UserVO 
{
	private String id;
	private String password;
	private String name;
	private String phoneNumber;
	private String sido_adress;
	private String sigungu_adress;
	private String email;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getSido_adress() {
		return sido_adress;
	}
	public void setSido_adress(String sido_adress) {
		this.sido_adress = sido_adress;
	}
	public String getSigungu_adress() {
		return sigungu_adress;
	}
	public void setSigungu_adress(String sigungu_adress) {
		this.sigungu_adress = sigungu_adress;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
}
